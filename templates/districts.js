function refreshDistrictColors() {
  if (!districtsLayer) return;
  districtsLayer.eachLayer(layer => {
    const name = layer.feature?.properties?.name;
    if (!name) return;
    const data = getDistrictAQI(name);
    layer.setStyle({ fillColor: data.color, color: data.color, fillOpacity: 0.35 });
    layer.unbindTooltip();
    layer.bindTooltip(
      `<b style="font-size:12px">${name}</b><br>AQI <b style="color:${data.color}">${data.aqi}</b> — ${data.label}<br>PM2.5: ${data.pm25} μg/m³`,
      { sticky: true, direction: 'center', className: 'district-tooltip' }
    );
  });
}

